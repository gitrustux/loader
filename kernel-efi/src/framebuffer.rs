/// Calculate max rows and columns based on framebuffer size
fn text_dimensions() -> (usize, usize) {
    let fb = info();
    let rows = fb.height / FONT_H;
    let cols = fb.width / FONT_W;
    (rows, cols)
}

/// Scroll the framebuffer content up by one line
fn scroll_up() {
    let fb = info();
    let (rows, _cols) = text_dimensions();

    if rows <= 1 {
        return; // Nothing to scroll
    }

    // Calculate pixel regions
    let src_start = FONT_H * fb.stride; // Skip first row
    let dst_start = 0;
    let line_size = FONT_H * fb.stride * 4; // Size in bytes of one text line
    let move_size = (rows - 1) * line_size; // All rows except the first

    unsafe {
        // Move all rows up by one line
        core::ptr::copy(
            fb.base.add(src_start * 4),
            fb.base.add(dst_start),
            move_size,
        );

        // Clear the last row
        let last_row_y = (rows - 1) * FONT_H;
        for dy in 0..FONT_H {
            let row = (last_row_y + dy) * fb.stride;
            for x in 0..fb.width {
                write_volatile((fb.base as *mut u32).add(row + x), 0x00000000);
            }
        }
    }
}

/// Draw a single glyph using font bitmap data
fn draw_glyph(c: char, x: usize, y: usize, fg_color: u32) {
    let fb = info();

    // Only handle printable ASCII (32-126)
    let ascii = if c.is_ascii() && c >= ' ' && c <= '~' {
        c as usize
    } else {
        return; // Skip unprintable characters
    };

    let font_offset = (ascii - 32) * 16;

    // Draw each row of the glyph
    for dy in 0..FONT_H {
        let font_row = FONT_DATA[font_offset + dy];
        let screen_y = (y * FONT_H + dy) * fb.stride;

        // Draw each pixel in the row
        for dx in 0..FONT_W {
            let mask = 1 << (7 - dx);
            if font_row & mask != 0 {
                let screen_x = x * FONT_W + dx;
                unsafe {
                    write_volatile(
                        (fb.base as *mut u32).add(screen_y + screen_x),
                        fg_color,
                    );
                }
            }
        }
    }
}

/// Write a string to the framebuffer console
pub fn write_str(s: &str) {
    let fg_color = encode_color(255, 255, 255); // White text
    let (_, cols) = text_dimensions();

    for b in s.bytes() {
        match b {
            b'\n' => unsafe {
                CURSOR_X = 0;
                CURSOR_Y += 1;
            },
            b'\r' => unsafe {
                CURSOR_X = 0;
            },
            b'\t' => unsafe {
                // Tab: advance to next multiple of 4
                CURSOR_X = (CURSOR_X + 4) & !3;
                if CURSOR_X >= cols {
                    CURSOR_X = 0;
                    CURSOR_Y += 1;
                }
            },
            ascii_char if ascii_char >= 32 && ascii_char <= 126 => unsafe {
                let (rows, _) = text_dimensions();

                // Check if we need to scroll
                if CURSOR_Y >= rows {
                    scroll_up();
                    CURSOR_Y = rows - 1;
                }

                // Check if we need to wrap to next line
                if CURSOR_X >= cols {
                    CURSOR_X = 0;
                    CURSOR_Y += 1;
                }

                // Check if we need to scroll after wrapping
                if CURSOR_Y >= rows {
                    scroll_up();
                    CURSOR_Y = rows - 1;
                }

                draw_glyph(ascii_char as char, CURSOR_X, CURSOR_Y, fg_color);
                CURSOR_X += 1;
            },
            _ => {
                // Skip unprintable characters
            }
        }
    }
}
