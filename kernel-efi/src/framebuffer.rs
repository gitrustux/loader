/// Plain-old-data framebuffer descriptor
#[derive(Copy, Clone)]
pub struct FramebufferInfo {
    pub base: *mut u8,
    pub size: usize,
    pub width: usize,
    pub height: usize,
    pub stride: usize,
    pub pixel_format: PixelFormat,
}

/// Global framebuffer state (valid after init)
static mut FB: Option<FramebufferInfo> = None;
static INITIALIZED: AtomicU8 = AtomicU8::new(0);

/// =========================================================
/// BOOT SERVICES PHASE
/// =========================================================

/// Query GOP and store framebuffer metadata
///
/// MUST be called before ExitBootServices.
///
/// This function:
/// 1. Opens GOP protocol
/// 2. Captures framebuffer info
/// 3. EXPLICITLY CLOSES GOP protocol
/// 4. Treats framebuffer as raw MMIO from then on
pub fn init() {
    // Find GOP handle using uefi::boot API
    let gop_handles = uefi::boot::locate_handle_buffer(SearchType::ByProtocol(&gop::GraphicsOutput::GUID))
        .expect("Failed to locate GOP");

    let gop_handle = *gop_handles
        .first()
        .expect("No GOP handle found");

    // Open GOP protocol using uefi::boot API
    let params = OpenProtocolParams {
        handle: gop_handle,
        agent: uefi::boot::image_handle(),
        controller: None,
    };

    let mut gop = unsafe {
        uefi::boot::open_protocol::<gop::GraphicsOutput>(params, OpenProtocolAttributes::Exclusive)
            .expect("Failed to open GOP")
    };

    let mode = gop.current_mode_info();
    let (width, height) = mode.resolution();
    let stride = mode.stride();

    // Detect pixel format
    let pixel_format = match mode.pixel_format() {
        gop::PixelFormat::Rgb => PixelFormat::Rgb,
        gop::PixelFormat::Bgr => PixelFormat::Bgr,
        _ => PixelFormat::Unknown,
    };

    let mut fb = gop.frame_buffer();
    let base = fb.as_mut_ptr();
    let size = fb.size();

    unsafe {
        FB = Some(FramebufferInfo {
            base,
            size,
            width,
            height,
            stride,
            pixel_format,
        });
        INITIALIZED.store(1, Ordering::SeqCst);
    }

    // ============================================================
    // CRITICAL: Drop GOP protocol before ExitBootServices
    // ============================================================
    //
    // The framebuffer pointer we captured is now owned by us.
    // UEFI firmware guarantees the framebuffer memory remains
    // valid and identity-mapped after ExitBootServices.
    //
    // By explicitly dropping gop here, we:
    // 1. Close the GOP protocol (uefi 0.36 uses RAII)
    // 2. Release the protocol handle
    // 3. Allow firmware to reclaim GOP-related memory
    // 4. Prevent firmware from invalidating framebuffer access
    //
    // After this drop, we treat the framebuffer as pure MMIO.
    // ============================================================
    drop(gop);

    // GOP CLOSED — framebuffer treated as raw MMIO from here on
}

/// Check if framebuffer was initialized
pub fn is_initialized() -> bool {
    INITIALIZED.load(Ordering::SeqCst) == 1
}

/// =========================================================
/// RUNTIME PHASE (NO UEFI BELOW)
/// =========================================================
///
// =========================================================
// RUNTIME INVARIANTS
// =========================================================
// - NO UEFI protocols (all closed before ExitBootServices)
// - NO heap usage
// - Framebuffer is treated as raw MMIO memory
// - Framebuffer pointer must remain identity-mapped
// - All writes are volatile MMIO writes
// =========================================================

#[inline(always)]
fn info() -> &'static FramebufferInfo {
    unsafe { FB.as_ref().expect("Framebuffer not initialized") }
}

/// Clear entire framebuffer to black
pub fn clear() {
    let fb = info();
    let pixels = fb.size / 4;
    let ptr = fb.base as *mut u32;

    for i in 0..pixels {
        unsafe {
            write_volatile(ptr.add(i), 0x00000000);
        }
    }
}

/// Write a solid horizontal line (debug primitive)
pub fn draw_scanline(y: usize, color: u32) {
    let fb = info();
    if y >= fb.height {
        return;
    }

    let row = fb.base as *mut u32;
    let offset = y * fb.stride;

    for x in 0..fb.width {
        unsafe {
            write_volatile(row.add(offset + x), color);
        }
    }
}

/// Convert RGB color into framebuffer-native format
#[inline(always)]
fn encode_color(r: u8, g: u8, b: u8) -> u32 {
    let fb = info();
    match fb.pixel_format {
        PixelFormat::Rgb => (r as u32) << 16 | (g as u32) << 8 | (b as u32),
        PixelFormat::Bgr => (b as u32) << 16 | (g as u32) << 8 | (r as u32),
        PixelFormat::Unknown => 0xFFFFFF,
    }
}

/// Emergency write: draws a single colored line at top
pub fn emergency_write(_s: &str) {
    // Draw a red line at the top of the screen as emergency marker
    let red = encode_color(255, 0, 0);
    draw_scanline(0, red);
}

